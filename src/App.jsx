function Store({children}) {
  const [state,dispatch] = useReducer(reducer,null,load);
  useEffect(()=>save(state),[state]);
  useEffect(()=>dispatch({type:"STREAK"}),[]);
  return <Ctx.Provider value={{state,dispatch}}>{children}</Ctx.Provider>;
}

