function reducer(state, action) {
  switch(action.type) {
    case "STREAK": {
      const today=new Date().toDateString();
      if (state.lastDay===today) return state;
      const y=new Date(); y.setDate(y.getDate()-1);
      return {...state, streak:state.lastDay===y.toDateString()?state.streak+1:1, lastDay:today};
    }
    case "START": {
      const {topic,mode} = action;
      const isMix = !topic||topic.id==="mixed";
      const q = mode===MODE.REVIEW ? Engine.buildReviewQueue(state.cards)
              : isMix             ? Engine.buildMixedQueue(state.cards,state.topics)
              :                     Engine.buildQueue(topic.id,state.cards,state.topics);
      return {...state, session:{topic:isMix?MIXED_TOPIC:topic,queue:q,mode:mode||MODE.NORMAL,idx:0,correct:0,total:0}};
    }
    case "ANSWER": {
      const {qid,correct,isOpen,answerTime=999} = action;
      const s=state.session; if(!s)return state;
      const prev=state.cards[qid]||{tc:0};
      const tc=correct?prev.tc+1:0;
      const intv=Engine.nextInterval(correct,tc);
      const next=new Date(); next.setDate(next.getDate()+intv);
      const topics={...state.topics};
      const tid=s.topic?.id;
      if (tid&&tid!=="mixed") {
        if (!topics[tid]) topics[tid]={correct:0,total:0};
        topics[tid]={correct:topics[tid].correct+(correct?1:0),total:topics[tid].total+1};
      }
      const newTotal=state.total+1, newCorrect=state.correct+(correct?1:0);
      const newOpenCorrect=state.openCorrect+(isOpen&&correct?1:0);
      const snap={...state,total:newTotal,correct:newCorrect,topics,openCorrect:newOpenCorrect,fastAnswers:state.fastAnswers+(correct&&answerTime<=EXAM_SECS?1:0)};
      const newAch=Engine.checkAchievements(snap,state.unlocked);
      return {
        ...state, total:newTotal, correct:newCorrect, openCorrect:newOpenCorrect, topics,
        cards:{...state.cards,[qid]:{tc,intv,next:next.toDateString(),seen:true}},
        unlocked:[...state.unlocked,...newAch.map(a=>a.id)],
        _newAch:newAch,
        session:{...s,correct:s.correct+(correct?1:0),total:s.total+1,idx:s.idx},
      };
    }
    case "NEXT":
      if(!state.session)return state;
      return {...state,session:{...state.session,idx:state.session.idx+1}};
    case "END": {
      const s=state.session;
      const perfect=s&&s.correct===s.total&&s.total>0;
      return {...state, lastPerfect:perfect,
        reviewSessions:state.reviewSessions+(s?.mode===MODE.REVIEW?1:0),
        examSessions:state.examSessions+(s?.mode===MODE.EXAM?1:0),
        session:{...s,done:true}};
    }
    case "DAILY_DONE":
      return {...state, lastDaily:new Date().toDateString(), dailyDone:(state.dailyDone||0)+1,
        _newAch:Engine.checkAchievements({...state,dailyDone:(state.dailyDone||0)+1},state.unlocked).filter(a=>!state.unlocked.includes(a.id))};
    case "AI_CHAT":
      return {...state, aiChats:(state.aiChats||0)+1};
    case "CLEAR_ACH": return {...state,_newAch:[]};
    case "RESET": localStorage.removeItem(STORAGE_KEY); return {...INIT};
    default: return state;
  }
}

