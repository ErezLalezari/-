const Engine = {
  shuffle(arr) { return [...arr].sort(()=>Math.random()-0.5); },

  shuffleOptions(q) {
    if (q.type !== Q.MULTIPLE) return q;
    return {...q, o: this.shuffle(q.o)};
  },

  diffLevel(topicStats={}, tid) {
    const ts = topicStats[tid];
    if (!ts || ts.total < 5) return "easy";
    const p = ts.correct / ts.total;
    return p >= 0.85 ? "hard" : p >= 0.65 ? "medium" : "easy";
  },

  sessionSize(topicStats={}, tid) {
    const r = ()=>Math.floor(Math.random()*2);
    return Math.min({"easy":5+r(),"medium":6+r(),"hard":7}[this.diffLevel(topicStats,tid)], SESSION_MAX);
  },

  buildQueue(tid, cardReviews={}, topicStats={}) {
    const count = this.sessionSize(topicStats, tid);
    const level = this.diffLevel(topicStats, tid);
    const today = new Date().toDateString();
    const scored = (DB[tid]||[]).map(q=>{
      const r = cardReviews[q.id];
      if (!r) return {q,pri:0};
      if (r.tc>=4 && level!=="hard") return {q,pri:3};
      return {q, pri: r.next<=today ? 1 : 2};
    });
    scored.sort((a,b)=>a.pri!==b.pri?a.pri-b.pri:Math.random()-0.5);
    return scored.slice(0,count).map(s=>this.shuffleOptions(s.q));
  },

  buildMixedQueue(cardReviews={}, topicStats={}) {
    const today = new Date().toDateString();
    const all = [];
    Object.entries(DB).forEach(([tid,qs])=>{
      qs.forEach(q=>{
        const r=cardReviews[q.id];
        const pri = r ? (r.tc>=4?3:r.next<=today?1:2) : 0;
        all.push({q:this.shuffleOptions(q),pri});
      });
    });
    return this.shuffle(all).sort((a,b)=>a.pri-b.pri).slice(0,SESSION_MAX).map(s=>s.q);
  },

  buildReviewQueue(cardReviews={}) {
    const wrongs = [];
    Object.values(DB).flat().forEach(q=>{
      const r=cardReviews[q.id];
      if (r && r.tc===0 && r.seen) wrongs.push(this.shuffleOptions(q));
    });
    return this.shuffle(wrongs).slice(0,SESSION_MAX);
  },

  nextInterval(correct, tc) {
    return correct ? (SRI[Math.min(tc,4)]||30) : SRI[0];
  },

  scoreLabel(pct) {
    if (pct>=90) return {label:"מצוינת! 🌟",stars:3};
    if (pct>=70) return {label:"כל הכבוד! ⭐",stars:2};
    if (pct>=50) return {label:"טוב, ממשיכים! 💪",stars:1};
    return {label:"נסי שוב! 🔄",stars:0};
  },

  diffLabel(ts,tid) {
    const map={easy:{t:"קלה 🌱",c:"#5DFC8A"},medium:{t:"בינונית ⚡",c:"#FFD700"},hard:{t:"מאתגרת 🔥",c:"#FF6B6B"}};
    return map[this.diffLevel(ts,tid)];
  },

  weakTopics(topicStats={}) {
    return TOPICS.filter(t=>{ const ts=topicStats[t.id]; return ts&&ts.total>=3&&ts.correct/ts.total<0.7; });
  },

  checkAchievements(state, prev=[]) {
    return ACHIEVEMENTS.filter(a=>!prev.includes(a.id)&&a.cond(state));
  },

  normalizeAnswer(s) { return s.trim().toLowerCase().replace(/['"״׳\-]/g,"").replace(/\s+/g," "); },
  checkOpen(input, accepted=[]) {
    const n=this.normalizeAnswer(input);
    return accepted.some(a=>{ const na=this.normalizeAnswer(a); return n===na||n.includes(na); });
  },

  nextReminder() {
    const now=new Date(), t=new Date(now);
    t.setHours(REMIND_H,REMIND_M,0,0);
    if (!SCHOOL_DAYS.includes(t.getDay())||t<=now) {
      t.setDate(t.getDate()+1); t.setHours(REMIND_H,REMIND_M,0,0);
    }
    while(!SCHOOL_DAYS.includes(t.getDay())) t.setDate(t.getDate()+1);
    return t;
  },
  msReminder() { return this.nextReminder()-new Date(); },
  reminderLabel() {
    const t=this.nextReminder(),d=t.getDay();
    return `יום ${["ראשון","שני","שלישי","רביעי","חמישי"][d]} ${String(t.getHours()).padStart(2,"0")}:${String(t.getMinutes()).padStart(2,"0")}`;
  },

  // Get today's daily challenge question
  getDailyQuestion() {
    const allQ = Object.values(DB).flat().filter(q=>q.type===Q.MULTIPLE);
    const seed  = Math.floor(Date.now()/(1000*60*60*24));
    return allQ[seed % allQ.length];
  },

  isDailyDone(lastDaily) {
    return lastDaily === new Date().toDateString();
  },
};

