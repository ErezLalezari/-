// ── QUIZ ─────────────────────────────────────
function Quiz({nav,params,online}) {
  const {state,dispatch}=useS();
  const [sel,setSel]=useState(null);
  const [openOk,setOpenOk]=useState(null);
  const [expl,setExpl]=useState("");
  const [loadExpl,setLoadExpl]=useState(false);
  const [hint,setHint]=useState(false);
  const [achs,setAchs]=useState([]);
  const [flash,setFlash]=useState(null);
  const [confetti,setConfetti]=useState([]);
  const [speaking,setSpeaking]=useState(false);
  const [timeLeft,setTimeLeft]=useState(EXAM_SECS);
  const [startTime,setStartTime]=useState(()=>Date.now());
  const timerRef=useRef(null);

  const topic=params?.topic;
  const mode=params?.mode||MODE.NORMAL;
  const isExam=mode===MODE.EXAM;
  const session=state.session;

  useEffect(()=>{ dispatch({type:"START",topic,mode}); },[]);

  useEffect(()=>{
    if(!isExam||!session)return;
    clearInterval(timerRef.current);
    setTimeLeft(EXAM_SECS);setStartTime(Date.now());
    timerRef.current=setInterval(()=>setTimeLeft(t=>{ if(t<=1){clearInterval(timerRef.current);answer("__timeout__");return 0;}return t-1; }),1000);
    return()=>clearInterval(timerRef.current);
  },[session?.idx,isExam]);

  useEffect(()=>{
    if(!session||isExam)return;
    const q=session.queue[session.idx];
    if(!q)return;
    const d=setTimeout(()=>{Audio.speak(q.q);setSpeaking(true);setTimeout(()=>setSpeaking(false),q.q.length*80);},300);
    return()=>{clearTimeout(d);Audio.stop();};
  },[session?.idx,isExam]);

  useEffect(()=>{ if(state._newAch?.length>0){setAchs(state._newAch);dispatch({type:"CLEAR_ACH"});} },[state._newAch]);

  if(!session)return null;
  const q=session.queue[session.idx];
  if(!q)return <div style={{padding:24,textAlign:"center",color:T.text}}><p>אין שאלות לחזרה! 🎉</p><Btn onClick={()=>nav("home")}>חזרה לבית</Btn></div>;

  const isMulti=q.type===Q.MULTIPLE;
  const answered=sel!==null||openOk!==null;
  const isLast=session.idx>=session.queue.length-1;
  const isCorrect=isMulti?sel===q.a:openOk===true;

  const burst=()=>{const p=Array.from({length:22},(_,i)=>({id:i,x:20+Math.random()*60,c:["#FFD700","#FF6B6B","#4ECDC4","#FFB347","#C3A6FF"][i%5],s:8+Math.random()*8,d:Math.random()*0.3,r:Math.random()*360}));setConfetti(p);setTimeout(()=>setConfetti([]),1800);};
  const doFlash=(c)=>{setFlash(c);setTimeout(()=>setFlash(null),400);};

  const answer=async(opt)=>{
    if(answered)return;
    clearInterval(timerRef.current);
    const at=Math.round((Date.now()-startTime)/1000);
    if(!isExam){Audio.stop();Audio.tap();}
    const timeout=opt==="__timeout__";
    const ok=timeout?false:(isMulti?opt===q.a:Engine.checkOpen(opt,q.acceptedAnswers||[]));
    setSel(timeout?"⏰ פג הזמן":opt);
    if(!isMulti)setOpenOk(ok);
    dispatch({type:"ANSWER",qid:q.id,correct:ok,isOpen:!isMulti,answerTime:at});
    if(ok){Audio.correct();Audio.vCorrect();doFlash("green");burst();}
    else{Audio.wrong();Audio.vWrong();doFlash("red");}
    const ca=isMulti?q.a:(q.acceptedAnswers||[])[0]||"";
    if(!isExam&&online){
      setLoadExpl(true);
      const e=await explainAnswer({q,correct:ok,userAnswer:opt,isCorrect:ok,isOpen:!isMulti}).catch(()=>ok?`✅ ${q.exp}`:`💡 ${ca}. ${q.exp}`);
      setExpl(e||"");setLoadExpl(false);
      setTimeout(()=>Audio.speak(e||"",{rate:0.85}),200);
    } else { setExpl(ok?`✅ ${q.exp}`:`💡 ${ca}. ${q.exp}`); }
  };

  const next=()=>{
    clearInterval(timerRef.current);Audio.stop();Audio.tap();
    if(isLast){if(session.correct===session.total)Audio.fanfare();dispatch({type:"END"});nav("result");return;}
    dispatch({type:"NEXT"});
    setSel(null);setOpenOk(null);setExpl("");setHint(false);
    setTimeLeft(EXAM_SECS);setStartTime(Date.now());
  };

  const optState=(opt)=>{ if(!answered)return"default"; if(opt===q.a)return"correct"; if(opt===sel)return"wrong"; return"default"; };
  const tc=topic?.cp||"#C3A6FF";

  return <div style={{position:"relative"}}>
    {flash&&<div style={{position:"fixed",inset:0,zIndex:8000,pointerEvents:"none",background:flash==="green"?"rgba(93,252,138,0.14)":"rgba(255,107,107,0.17)",animation:"flashFade 0.4s ease forwards"}}/>}
    {confetti.map(p=><div key={p.id} style={{position:"fixed",top:"30%",left:`${p.x}%`,width:p.s,height:p.s,background:p.c,borderRadius:3,zIndex:8888,pointerEvents:"none",animation:`confettiFall 1.6s ${p.d}s ease-out forwards`,transform:`rotate(${p.r}deg)`}}/>)}
    <AchPopup achs={achs} onClose={()=>setAchs([])}/>

    {/* Header */}
    <div style={{display:"flex",alignItems:"center",gap:10,marginBottom:12}}>
      <BackBtn onClick={()=>{clearInterval(timerRef.current);Audio.stop();nav("home");}}/>
      <div style={{flex:1}}>
        <div style={{display:"flex",justifyContent:"space-between",fontSize:12,color:T.muted,marginBottom:5}}>
          <span>{mode===MODE.REVIEW?"🔄":mode===MODE.EXAM?"🎓":topic?.emoji+" "+topic?.name} • {session.idx+1}/{session.queue.length}</span>
          <span style={{color:T.gold}}>{session.correct}✓</span>
        </div>
        <Bar value={session.idx+1} max={session.queue.length} color={tc} height={5}/>
      </div>
    </div>

    {/* Exam timer */}
    {isExam&&!answered&&<div style={{marginBottom:10}}>
      <div style={{display:"flex",justifyContent:"space-between",fontSize:12,marginBottom:3}}>
        <span style={{color:timeLeft>15?T.success:timeLeft>7?T.gold:T.danger,fontWeight:700}}>⏱ {timeLeft}s</span>
        <span style={{color:T.muted,fontSize:11}}>מבחן – אין רמזים</span>
      </div>
      <Bar value={timeLeft/EXAM_SECS*100} color={timeLeft>15?T.success:timeLeft>7?T.gold:T.danger} height={4}/>
    </div>}

    {/* Question */}
    <Card style={{textAlign:"center",padding:"26px 18px"}}>
      <div style={{fontSize:42,marginBottom:10}}>{mode===MODE.EXAM?"🎓":topic?.emoji||"📖"}</div>
      <h2 style={{fontSize:19,fontWeight:800,lineHeight:1.65,margin:0}}>{q.q}</h2>
      {!isExam&&<div style={{display:"flex",justifyContent:"center",gap:8,marginTop:12,flexWrap:"wrap"}}>
        {!answered&&<button onClick={()=>setHint(h=>!h)} style={{background:"none",border:`1px solid ${T.border}`,borderRadius:T.r.xs,padding:"5px 12px",color:T.muted,cursor:"pointer",fontSize:12}}>{hint?"🙈 הסתר":"💡 רמז"}</button>}
        <button onClick={()=>{ if(speaking){Audio.stop();setSpeaking(false);}else{Audio.speak(q.q);setSpeaking(true);setTimeout(()=>setSpeaking(false),q.q.length*80);}}} style={{background:speaking?`${T.gold}20`:"none",border:`1px solid ${speaking?T.gold:T.border}`,borderRadius:T.r.xs,padding:"5px 12px",color:speaking?T.gold:T.muted,cursor:"pointer",fontSize:12}}>{speaking?"🔊":"🔈"}</button>
      </div>}
      {hint&&!isExam&&<div style={{marginTop:10,padding:"8px 14px",background:`${T.gold}16`,borderRadius:T.r.sm,color:T.gold,fontSize:13}}>💡 {q.hint}</div>}
      {q.isAI&&<div style={{marginTop:8,fontSize:10,color:"#00C9FF",opacity:0.7}}>✨ שאלה שנוצרה ע"י AI</div>}
    </Card>

    {/* Options */}
    {isMulti?<div>{(q.o||[]).map((opt,i)=><AnswerOpt key={i} opt={opt} idx={i} state={optState(opt)} onClick={()=>answer(opt)}/>)}</div>
      :<OpenInput onSubmit={answer} disabled={answered} correct={openOk}/>}

    {/* Explanation */}
    {answered&&<Card glow color={isCorrect?T.success:T.danger} style={{borderColor:isCorrect?`${T.success}44`:`${T.danger}44`}}>
      <div style={{fontSize:34,textAlign:"center",marginBottom:8}}>{isCorrect?"🌟":"💪"}</div>
      {loadExpl?<div style={{textAlign:"center",color:T.muted,animation:"pulse 1.2s infinite"}}>✨ מסביר...</div>
        :<div style={{display:"flex",alignItems:"flex-start",gap:8}}>
          <p style={{margin:0,lineHeight:1.8,fontSize:14,textAlign:"right",flex:1}}>{expl}</p>
          {!isExam&&<button onClick={()=>Audio.speak(expl,{rate:0.85})} style={{background:"none",border:`1px solid ${T.border}`,borderRadius:T.r.xs,padding:"5px 8px",color:T.muted,cursor:"pointer",fontSize:15,flexShrink:0}}>🔈</button>}
        </div>}
      {!loadExpl&&<Btn v={isCorrect?"success":"primary"} onClick={next} style={{marginTop:12,marginBottom:0}}>{isLast?"סיום! 🏁":"הבאה ▶"}</Btn>}
    </Card>}
  </div>;
}

